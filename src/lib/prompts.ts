interface QuizContext {
  aiUsage?: string;
  jobChange?: string;
  learning?: string;
}

const AI_USAGE_MAP: Record<string, string> = {
  daily: "毎日使っている",
  weekly: "週に数回",
  rarely: "ほとんど使わない",
  never: "全く使わない",
};

const JOB_CHANGE_MAP: Record<string, string> = {
  none: "転職経験なし",
  once: "1回",
  few: "2〜3回",
  many: "4回以上",
};

const LEARNING_MAP: Record<string, string> = {
  active: "積極的に学んでいる",
  sometimes: "たまに学ぶ",
  passive: "必要に迫られたら",
  none: "特に学んでいない",
};

export function getSystemPrompt(quiz?: QuizContext): string {
  let quizSection = "";
  if (quiz && (quiz.aiUsage || quiz.jobChange || quiz.learning)) {
    const lines: string[] = [];
    lines.push("\n【ユーザー情報（診断に活用すること）】");
    if (quiz.aiUsage)
      lines.push(
        `・AI活用度：${AI_USAGE_MAP[quiz.aiUsage] || quiz.aiUsage}`
      );
    if (quiz.jobChange)
      lines.push(
        `・転職経験：${JOB_CHANGE_MAP[quiz.jobChange] || quiz.jobChange}`
      );
    if (quiz.learning)
      lines.push(
        `・学習姿勢：${LEARNING_MAP[quiz.learning] || quiz.learning}`
      );

    if (quiz.aiUsage === "never") {
      lines.push(
        "→ AI未使用は深刻なリスク要因。適応力スコアに大きく影響させること。"
      );
    } else if (quiz.aiUsage === "daily") {
      lines.push(
        "→ 日常的にAIを活用している。拡張ポテンシャルの評価にプラスすること。"
      );
    }

    if (quiz.learning === "none") {
      lines.push(
        "→ 学習習慣がないのは致命的。WEFの「5年以内に44%がリスキリング必要」データを引用して警告すること。"
      );
    }

    if (quiz.jobChange === "none") {
      lines.push(
        "→ 転職経験ゼロは適応力の低さを示唆する。ただし同一職種での深い専門性がある可能性も考慮すること。"
      );
    }

    quizSection = lines.join("\n");
  }

  return SYSTEM_PROMPT + quizSection + "\n" + OUTPUT_FORMAT;
}
